#include "ft_lst.h"

int	ft_lstsize(t_lst *lst)
{
	int		res;
	t_lst	*aux;

	res = 0;
	aux = lst;
	while (aux != NULL)
	{
		aux = aux->next;
		res++;
	}
	return (res);
}
