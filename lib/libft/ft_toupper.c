#include "libft.h"

int	ft_toupper(int c)
{
	unsigned char	aux;

	aux = (unsigned char)c;
	if (c >= 97 && c <= 122)
	{
		aux -= 32;
		return ((int)aux);
	}
	else
		return (c);
}
